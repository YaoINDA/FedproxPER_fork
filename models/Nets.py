class LSTMShakespeare(nn.Module):
    # def __init__(self, vocabsize, gpu, embedding_dim=256, n_hidden=1024, n_layers=1):
    def __init__(self, vocabsize, gpu, embedding_dim=8, n_hidden=100, n_layers=2):
        super(LSTMShakespeare, self).__init__()
        self.embedding = nn.Embedding(vocabsize, embedding_dim)
        self.lstm = nn.LSTM(embedding_dim, n_hidden, n_layers)
        self.linear = nn.Linear(n_hidden, vocabsize)
        self.gpu = gpu
        self.n_layers = n_layers
        self.n_hidden = n_hidden

    def forward (self, x, hidden):
        embed = self.embedding(x)
        output, hidden = self.lstm(embed, hidden)
        logits = self.linear(output)
        return logits, hidden

    def init_hidden(self, n_sequence_length=80):
        ''' Initializes hidden state '''
        weight = next(self.parameters()).data

        if self.gpu > -1 :
            hidden = (weight.new(self.n_layers, n_sequence_length, self.n_hidden).zero_().cuda(),
                      weight.new(self.n_layers, n_sequence_length, self.n_hidden).zero_().cuda())
        else:
            hidden = (weight.new(self.n_layers, n_sequence_length, self.n_hidden).zero_(),
                      weight.new(self.n_layers, n_sequence_length, self.n_hidden).zero_())

        return hidden
